// 均匀采样确保每个分类都覆盖全色域
function sampleMard(n: number): string[] {
  const all = MARD_291.map(c => c.id);
  if (n >= all.length) return [...all];
  const step = all.length / n;
  const result: string[] = [];
  for (let i = 0; i < n; i++) result.push(all[Math.floor(i * step)]);
  return result;
}

export const MARD_CATEGORIES: Record<MardCategory, string[]> = {
  '72': sampleMard(72),
  '96': sampleMard(96),
  '120': sampleMard(120),
  '144': sampleMard(144),
  '168': sampleMard(168),
  'all': [...MARD_291.map(c => c.id)],
};

export function getPalette(brand: string): BeadColor[] {
  return PALETTES[brand] ?? MARD_291;
}

export function getBrandNames(): string[] {
  return Object.keys(PALETTES);
}

export function filterMardPalette(palette: BeadColor[], category: MardCategory): BeadColor[] {
  const allowed = new Set(MARD_CATEGORIES[category] ?? []);
  return palette.filter(c => allowed.has(c.id));
}
